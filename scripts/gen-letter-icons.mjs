/**
 * 为缺失图标的站点生成首字母 SVG
 * 用法: node scripts/gen-letter-icons.mjs
 */
import fs from 'fs'
import path from 'path'
import { fileURLToPath } from 'url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))

const COLORS = [
  '#6366f1', '#8b5cf6', '#a855f7', '#d946ef',
  '#ec4899', '#f43f5e', '#ef4444', '#f97316',
  '#eab308', '#84cc16', '#22c55e', '#14b8a6',
  '#06b6d4', '#0ea5e9', '#3b82f6', '#6366f1',
]

function letterSvg(name, colorIdx) {
  const letter = name.charAt(0).toUpperCase()
  const bg = COLORS[colorIdx % COLORS.length]
  const svg = `<svg xmlns="http://www.w3.org/2000/svg" width="64" height="64" viewBox="0 0 64 64">
<rect width="64" height="64" rx="14" fill="${bg}"/>
<text x="32" y="44" text-anchor="middle" font-family="system-ui,sans-serif" font-size="32" font-weight="600" fill="white">${letter}</text>
</svg>`
  return 'data:image/svg+xml;base64,' + Buffer.from(svg).toString('base64')
}

async function main() {
  const dataPath = path.resolve(__dirname, '../src/data/ai-sites.js')
  const mod = await import('file://' + dataPath.replace(/\\/g, '/'))
  const sites = mod.AI_SITES
  const categories = mod.AI_CATEGORIES

  let filled = 0
  sites.forEach((site, i) => {
    if (!site.icon_url) {
      site.icon_url = letterSvg(site.name, i)
      filled++
      console.log(`Generated: ${site.name} -> [${site.name.charAt(0)}]`)
    }
  })

  const output = `// Auto-generated by scripts/fetch-ai-icons.mjs — DO NOT EDIT
export const AI_CATEGORIES = ${JSON.stringify(categories, null, 2)}

export const AI_SITES = ${JSON.stringify(sites, null, 2)}
`
  fs.writeFileSync(dataPath, output, 'utf-8')
  console.log(`\nFilled ${filled} missing icons. Total: ${sites.length}`)
}

main().catch(console.error)
